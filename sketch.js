var dog,sadDog,happyDog;


function preload(){
  sadDog=loadImage("Images/Dog.png");
  happyDog=loadImage("Images/happy dog.png");
}

function setup() {
  createCanvas(1000,600);
  dog=createSprite(590,500,600,900);
  dog.addImage(happyDog);
  dog.scale=0.15;
  dog=createSprite(600,100,500,200);
  dog.addImage(sadDog);
  dog.scale=0.15;

}

function draw() {
  background(46,139,87);
  drawSprites();
}

